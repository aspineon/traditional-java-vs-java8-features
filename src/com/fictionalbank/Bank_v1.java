  private Optional<String> transactions(String flag, double amount) {

    if (flag.contentEquals("n")) {
      List<String[]> transList = transactionMaster.get("1234");
      String[] singleTrans = transList.get(transList.size() - 1);
      return Optional.ofNullable(singleTrans[3]);
    }

    if (flag.contentEquals("c")) {
      List<String[]> transList = transactionMaster.get("1234");
      String[] singleTrans = transList.get(transList.size() - 1);
      Double currBal = Double.parseDouble(singleTrans[3]) + amount;
      String[] creditTrans = { "03-Sep-2019", "Credit", "1500.00", currBal.toString() };
      list0.add(creditTrans);
      transactionMaster.put("1234", list0);
    }

    if (flag.contentEquals("d")) {
      // HashMap, or for that matter, Map does not have streams functionality because
      // Map does not extend Collection interface which is where the strems are.
      List<String[]> transList = transactionMaster.get("1234");
      String[] singleTrans = transList.get(transList.size() - 1);
      Double currBal = Double.parseDouble(singleTrans[3]) - amount;
      String[] debitTrans = { "03-Sep-2019", "debit", "1500.00", currBal.toString() };
      list0.add(debitTrans);
      transactionMaster.put("1234", list0);
    }

    if (flag.contentEquals("tt-debit")) {
      List<String[]> transList = transactionMaster.get("1234");

      transList.stream().filter(allTrans -> allTrans[1].equalsIgnoreCase("debit"))
          .forEach(debitTrans -> System.out.println(
              debitTrans[0] + " " + debitTrans[1] + " " + debitTrans[2] + " " + debitTrans[3]));
    }

    if (flag.contentEquals("tt-credit")) {
//      long startTime = System.currentTimeMillis();
      List<String[]> transList = transactionMaster.get("1234");
      transList.stream().filter(allTrans -> allTrans[1].equalsIgnoreCase("credit"))
          .forEach(creditTrans -> System.out.println(
              creditTrans[0] + " " + creditTrans[1] + " " + creditTrans[2] + " " + creditTrans[3]));

//      long endTime = System.currentTimeMillis();
//      System.out.println("Total Time Taken:"+ (endTime-startTime) +" ms");
    }

    return Optional.empty();

  }

